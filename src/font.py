def write(screen, char, startX, startY):
    ch = font[char]
    y = startY
    for i, row in enumerate(ch):
        x = startX
        for j, c in enumerate(row):
            if c:
                screen.pixel(x, y, 1)
            x += 1
        y += 1
        
def printString(screen, text, startX, startY):
    x = startX
    y = startY
    for char in text:
        write(screen, char, x, y)
        x += 16
